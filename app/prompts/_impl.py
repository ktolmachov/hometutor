def infer_homework_level_from_message(text: str) -> str | None:
    """
    Эвристика по первым символам сообщения в чате тьютора.

    Не срабатывает на произвольные вопросы без явных маркеров — только на фразы
    вроде «подсказка», «дай план», «разбери ошибку», «полное решение».
    """
    raw = (text or "").strip()
    if not raw:
        return None
    low = raw[:220].lower()
    if any(
        x in low
        for x in (
            "полное решение",
            "полный ответ",
            "full solution",
            "complete solution",
            "решение целиком",
        )
    ):
        return "full_solution"
    if any(
        x in low
        for x in (
            "разбери ошиб",
            "разбор ошиб",
            "моя ошибк",
            "мою ошиб",
            "где я ошиб",
            "error_review",
            "review my",
        )
    ):
        return "error_review"
    if any(
        x in low
        for x in (
            "дай план",
            "план реш",
            "по шагам",
            "структур",
            " plan",
            "plan ",
        )
    ):
        return "plan"
    if any(
        x in low
        for x in (
            "hint",
            "подсказ",
            "намёк",
            "намек",
        )
    ):
        return "hint"
    return None
